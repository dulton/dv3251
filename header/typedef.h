#ifndef _TYPEDEF_H_
#define _TYPEDEF_H_

#include <stdbool.h>

typedef bool BOOL;
typedef unsigned  char  BYTE;
typedef unsigned  short WORD;
typedef unsigned  long  DWORD;

typedef unsigned char	u8, U8, uint8, UINT8;
typedef signed   char	s8, S8, int8, INT8;

typedef unsigned short	u16, U16, uint16, UINT16;
typedef signed   short	s16, S16, int16, INT16;

typedef unsigned long	u32, U32, uint32, UINT32;
typedef signed   long	s32, S32, int32, INT32;

typedef unsigned long long	u64, U64, uint64, UINT64;
typedef signed long long	s64, S64, int64, INT64;

typedef unsigned int uint;
typedef unsigned char uchar;


#define  TRUE   1
#define  FALSE	0

#define  ON   	1
#define  OFF	0

#endif