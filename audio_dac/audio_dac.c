void wr_reg(u8 addr, u32 reg_dat)
{
    REG32(AT_ADR) = (1<<30) | (addr<<24) | reg_dat;
    while((REG32(AT_ADR) & (1<<31)) == (1<<31));
}

u32 rd_reg(u8 addr)
{
	REG32(AT_ADR) = (addr<<24);
    while((REG32(AT_ADR) & (1<<31)) == (1<<31));
    return (u32)(REG32(AT_DAT));
}

volatile u8 dac_block_flag;
void dac_irq(void)
{
	if((rd_reg(OBUF_STA) & 0x01) == 0x01)
	{
		//deg_Printf("dac isr\n");
		wr_reg(OBUF_CLR, 0x02);    // clear obuf pending
		wr_reg(FIFO_CNT, 2048-2);
		g_AudioFrameBuf.valid = 0;
	}
}

void digital_volume_set(u16 volume, u8 mode, u8 step)
{
	while((rd_reg(DACSTA) & (1<<4)) == 0);	// wait volume pending
	wr_reg(DACCLR, (1<<4));		// clear volume pending
	wr_reg(DACVOL, volume);
	wr_reg(DACVCON, 0x04 | (mode<<4) | step);
}


void dac_analog_init()
{
	static u8 dac_analog_init_flag=0;
	
	if(dac_analog_init_flag)
		return;
	//EN_DACBIAS_12 EN_VDDHP_12 EN_VCM_12 EN_VDDAC_12 SEL_VCM_INT_12V SEL_VDDDAC_OPD_12  multi bits DAC
	REG32(DAC_ACON0) |= BIT(0)|BIT(17)|BIT(15)|BIT(16)|BIT(27)|BIT(28)|BIT(18);
	Delay_MS(5);
	//EN_DAC_12V EN_LPF_12V
	REG32(DAC_ACON0) |= BIT(3)|BIT(7);
	//?7fff?dac????
	Delay_MS(1);
	//EN_PD_12V
	REG32(DAC_ACON0) |= BIT(13);
	//EN_POPS_12V
	REG32(DAC_ACON0) |= BIT(14);
	//VCM_INT_SEL VDDDAC_OPD
	REG32(DAC_ACON0) &= ~(BIT(27)|BIT(28));
	Delay_MS(10);
	//HP ??
	//DAC2HP_EN
	REG32(DAC_ACON0) |= BIT(4);
	//PD_EN
	REG32(DAC_ACON0) &= ~BIT(13);
	//HP_EN DACTCMP_EN
	REG32(DAC_ACON0) |= BIT(6)|BIT(1);
	Delay_MS(1);
	//POPS_EN
	REG32(DAC_ACON0) &= ~BIT(14);
	//DACTCMP_EN
	REG32(DAC_ACON0) &= ~BIT(1);

	REG32(DAC_ACON1) = 0xF8;//analog gain
	dac_analog_init_flag=1;
}


void obuf_cfg()
{
	wr_reg(DACCFG, rd_reg(DACCFG)|OBUF_EN);//BOUF enable
    wr_reg(OBUF_CLR, 0x03);
    wr_reg(OBUF_SIZE, 64);
    wr_reg(OBUF_BLK, 2048-2);
    wr_reg(OBUF_CFG, (2<<5) |OBUF_PND_INT);
	dac_analog_init(); 
    SetIntSrc(OBUF_INT);
}



