//======delay_flag : 0 init not delay , 1 init need delay====first time use it or after play avi will set delay flag====
void music_init(u32 *tempbuf,u16 volume,u8 sample,u8 delay_flag)
{

	//deg("key voice init start\n");
	REG32(PCON0)   &= ~(1<<21);    // enable dac clock
	REG32(CLKCON1) &= ~(0xf<<8);
	REG32(CLKCON1) |= (9<<8);//240/(9+1)=24M
	REG32(CLKCON0) &= ~((1<<6)|(1<<7));

	wr_reg(DACCFG, rd_reg(DACCFG)|OBUF_EN);//BOUF enable
       wr_reg(OBUF_CLR, 0x03);
       wr_reg(OBUF_SIZE, 64);
       wr_reg(OBUF_BLK, MUSIC_LEN);
       //wr_reg(OBUF_CFG, (2<<5) |OBUF_PND_INT);	// int 
	wr_reg(OBUF_CFG, (2<<5) );				//not int
       dac_analog_init();

	REG32(DAC_ACON0) |= BIT(2);
       wr_reg(FIFO_CNT, MUSIC_LEN*2);
	//wr_reg(FIFO_CNT, 0);

    //Rout = Lin*DACLRMIX1[7:0]/128 + Rin*DACLRMIX1[15:8]/128
//    wr_reg(DACLRMIX1,0x007f);

	
       music_llp0.SAR = (u32)tempbuf;
	music_llp1.SAR = (u32)(tempbuf+MUSIC_LEN);
	
   	llp_mem2peri (AUDIO_DAC_DMA_CH, (0<<7)|(7<<11), 0, (DMA_LLP *)(&music_llp0));
   	wr_reg(DACCFG, (rd_reg(DACCFG)&(~(0xf<<4))) | (sample<<4) | 0x01);//16K
	digital_volume_set(volume,0,0);//
	audio_amplifier_open();
	if(delay_flag)
	{
		Delay_MS(800);					//set vol need wait sometimes
	}

}

void music_play(u8 *srcbuf ,u32 buf_len)
{
	REG32(DAC_ACON0) &= ~BIT(2);
	while(buf_len > MUSIC_LEN*2)					//data is 16bit
	{
		if((rd_reg(OBUF_STA) & 0x01) == 0x01)
		{
			wr_reg(OBUF_CLR, 0x02);    			// clear obuf pending

			srcbuf += MUSIC_LEN*2;				

			if(!(rd_reg(OBUF_STA)&0x3C))		//dac get 0 blk, will write 1 blk
			{
				int i;
				u32 *dst_buf = (u32*)music_llp1.SAR;
				for(i = 0; i < MUSIC_LEN; i++)
				{
					*dst_buf = srcbuf[i*2] | srcbuf[i*2+1]<<8 | srcbuf[i*2]<<16 | srcbuf[i*2+1]<<24;
					dst_buf++;
				}
			}
			else
			{
				int i;
 				u32 *dst_buf = (u32*)music_llp0.SAR;
				for(i = 0; i < MUSIC_LEN; i++)
				{
					*dst_buf = srcbuf[i*2] | srcbuf[i*2+1]<<8 | srcbuf[i*2]<<16 | srcbuf[i*2+1]<<24;
					dst_buf++;
				}
			}

			wr_reg(FIFO_CNT, MUSIC_LEN*2);
			buf_len -= MUSIC_LEN*2;
		}
	}
	my_memset((void *)music_llp0.SAR, 0, MUSIC_LEN*4);
	my_memset((void *)music_llp1.SAR, 0, MUSIC_LEN*4);
}

