void task_record_deal_msg(u8 msg)
{
	switch(msg)
	{
	case T_KEY_PLAY | T_KEY_SHORT_UP:

		Delay_MS(100);
		if((rd_reg(OBUF_STA) & 0x01) == 0x00)
		{
			deg_Printf("task_record_deal_msg waiting for OBUF_STA\r\n");
			Delay_MS(100);
		}

		t_record_video.recording_sta = 1;     //Ã¨Â¿ÂÃ¥ÂÂ¥Ã¥Â½ÂÃ¥ÂÂÃ§ÂÂ¶Ã¦Â?
		break;
	case T_KEY_UP | T_KEY_SHORT_UP:
		video_enlager++;
		if(video_enlager>MAX_ENLAGER_FACTOR)
		{
			video_enlager = MAX_ENLAGER_FACTOR;
		}
		tvout_Change_dis_size(video_enlager,0);
		//t_record_video.display_update = 1;
		break;
	case T_KEY_DOWN | T_KEY_SHORT_UP:
		if(video_enlager>0)
		{
			video_enlager--;
		}
		tvout_Change_dis_size(video_enlager,0);
		//t_record_video.display_update = 1;
		break;
	case T_KEY_MENU | T_KEY_SHORT_UP:
		#if(MENU_DISPLAY_USE == MENU_DISPLAY_ENABLE)
		MenuDisplay(MENU_VIDEO);
		#endif
		task_record_clean_system_time();
		break;
	default:
		deal_msg(msg);
		break;
	}
}

